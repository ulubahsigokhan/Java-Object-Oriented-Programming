package com.gokhanul.classes;

public class JavaConstructor {

    // CONSTRUCTOR
    // A constructor is a special method that is automatically called when an object
    // of a class is created. Its main purpose is to initialize variables (fields) inside
    // the object. It looks like a method, but it has no return type and its name must match
    // the class name/

    String java;
    int year;

    public JavaConstructor(String java, int year) {
        this.java = java;
        this.year = year;

    }
}
